    <article id="typography" class="section scrollspy">
        <h2>Typography</h2>
        <p>
            The term comes from ancient Greek and is best translated as "working with scripture".
            Nowadays, typography is an important part in any design process. The right font choice and font size
            are crucial for the information to be transmitted. Line spacing, letter spacing and line length
            are other important points that improve the readability of information. The templates under this point deal with the
            Presentation of information.
        </p>
    </article>
