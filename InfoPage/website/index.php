                  <div class="col-lg-4">
                      <h4>Ausgangssitutaion</h4>
                      <p>Wir möchten Menschen mit wenig Vorwissen für das Schreiben von Webseiten begeistern und mit Hilfe
                          von spielerischen Übungen, vorgefertigten Lern-Templates und einer Code-Bibliothek eine
                          Hilfestellung anbieten. Durch ‘Learning by doing’ hat man die Möglichkeit, sich schnell in
                          verschiedene Programmiersprachen einzuarbeiten.</p>
                  </div>

                  <div class="col-lg-4">
                      <h4>Problemstellung</h4>
                      <p>Meistens ist es für Schüler der unteren Schulstufen schwer, den Zusammenhang einzelner
                          Komponenten einer Dokumentation zu erkennen und sie kommen deswegen nur schwer mit den
                          zahlreichen, verschiedenen Kommandos zurecht. Wir möchten das endlose Stöbern in Spezifikationen
                          erleichtern, indem wir praxisbezogene Übungsbeispiele bereitstellen.</p>

                  </div>

                  <div class="col-lg-4">
                      <h4>Warum?</h4>
                      <p>Da wir aus eigener Erfahrung in den ersten HTL-Jahren wenig Motivation für die Gestaltung von
                          Webseiten aufbringen konnten, ist eines der Ziele, mit einem grafisch ansprechenden Design die
                          Aktualität dieser Techniken aufzuzeigen.</p>

